public class MenuGroupItem {

    private String title;

    private Integer imageResource;

    private Integer imageResourceSelected;

    public String getTitle() {
        return title;
    }

    public void setTitle(String title) {
        this.title = title;
    }

    public Integer getImageResource() {
        return imageResource;
    }

    public void setImageResource(Integer imageResource) {
        this.imageResource = imageResource;
    }

    public Integer getImageResourceSelected() {
        return imageResourceSelected;
    }

    public void setImageResourceSelected(Integer imageResourceSelected) {
        this.imageResourceSelected = imageResourceSelected;
    }
}
